/// Helper for requests whose result type is `Option<T>` (e.g. hover).
async fn call_request_optional<T: serde::de::DeserializeOwned>(
    service: &mut LspService<Backend>,
    method: &'static str,
    id: i64,
    params: impl serde::Serialize,
) -> Option<T> {
    let req = JsonRpcRequest::build(method)
        .id(id)
        .params(serde_json::to_value(params).unwrap())
        .finish();
    let resp = service
        .call(req)
        .await
        .unwrap()
        .unwrap_or_else(|| panic!("{method} returned no response"));
    let (_id, result) = resp.into_parts();
    let value = result.unwrap_or_else(|e| panic!("{method} response had error: {e:?}"));
    if value.is_null() {
        None
    } else {
        Some(
            serde_json::from_value(value)
                .unwrap_or_else(|e| panic!("{method} result deserialise failed: {e}")),
        )
    }
}

/// Translate a UTF-8 byte offset in `src` into the (line, UTF-16-char)
/// LSP Position used by the integration-test request bodies.
fn byte_to_line_char(src: &str, byte: usize) -> (u32, u32) {
    let mut line: u32 = 0;
    let mut col: u32 = 0; // UTF-16 code units
    let mut consumed: usize = 0;
    for c in src.chars() {
        if consumed >= byte {
            break;
        }
        if c == '\n' {
            line += 1;
            col = 0;
        } else {
            col += c.len_utf16() as u32;
        }
        consumed += c.len_utf8();
    }
    (line, col)
}

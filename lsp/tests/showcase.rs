#[tokio::test]
async fn workspace_index_populated_after_initialize() {
    let (mut service, _docs, index, _sink) = spawn_service_full().await;
    initialize_with_root(&mut service, Some(SHOWCASE_ROOT)).await;

    // Poll up to 5s for the eager_parse background task to finish.
    // Threshold: the showcase has 19 .deal/.dealx files producing ≥40
    // FQ-path entries (measured empirically: 48 at the time of authoring).
    // We use 40 to leave headroom for showcase-edit drift while still
    // proving eager_parse actually executed (not just spun up).
    let n = wait_for_index(&index, 40, Duration::from_secs(5)).await;
    assert!(
        n >= 40,
        "eager_parse should populate ≥40 symbols on the showcase, got {n}"
    );
    // Spot-check: ThermallyManaged + BatteryPack are showcase staples;
    // they must be in the in-memory index under SOME PathString. The
    // exact prefix depends on which file declared which `package`
    // statement — we accept any path whose suffix matches the type name.
    let snap = index.snapshot();
    let paths: Vec<String> = snap.iter().map(|(p, _)| p.clone()).collect();
    let has_suffix = |needle: &str| -> bool {
        paths
            .iter()
            .any(|p| p == needle || p.ends_with(&format!(".{needle}")))
    };
    assert!(
        has_suffix("ThermallyManaged"),
        "expected an index entry ending in .ThermallyManaged. Top paths: {:?}",
        &paths[..paths.len().min(10)]
    );
    assert!(
        has_suffix("BatteryPack"),
        "expected an index entry ending in .BatteryPack. Top paths: {:?}",
        &paths[..paths.len().min(10)]
    );
}


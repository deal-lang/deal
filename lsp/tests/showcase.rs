// Test 1: diagnostics_match_phase_2_codes
// ---------------------------------------------------------------------

#[tokio::test]
async fn diagnostics_match_phase_2_codes() {
    let (mut service, _docs, sink) = spawn_service().await;
    initialize(&mut service).await;

    let src = std::fs::read_to_string(MALFORMED_SAMPLE).expect("malformed sample missing");
    let uri = Url::from_file_path(std::fs::canonicalize(MALFORMED_SAMPLE).unwrap())
        .expect("file:// URL build");

    did_open(&mut service, &uri, &src, 1).await;
    // Give the background sink task a moment to drain the notification.
    tokio::time::sleep(Duration::from_millis(50)).await;

    let pubs = collected_diagnostics(&sink).await;
    assert!(
        !pubs.is_empty(),
        "expected ≥1 publishDiagnostics notification after did_open"
    );
    let last = pubs.last().unwrap();
    assert_eq!(last.uri, uri);
    assert!(
        !last.diagnostics.is_empty(),
        "expected ≥1 diagnostic for malformed sample"
    );
    // Phase 2 envelope codes start with 'E' (E0xxx parser, E2xxx sema).
    let code_strs: Vec<String> = last
        .diagnostics
        .iter()
        .filter_map(|d| match &d.code {
            Some(NumberOrString::String(s)) => Some(s.clone()),
            _ => None,
        })
        .collect();
    assert!(
        code_strs.iter().any(|c| c.starts_with('E')),
        "expected at least one E-code diagnostic, got {code_strs:?}"
    );
    // Sema-level error E2100 is the expected code for this fixture
    // (verified live via dump_probe in Task 3).
    assert!(
        code_strs.iter().any(|c| c == "E2100"),
        "expected E2100 (sema undefined type) for m01 fixture, got {code_strs:?}"
    );
    // Source field must identify deal-lsp (so VS Code groups them).
    assert_eq!(last.diagnostics[0].source.as_deref(), Some("deal-lsp"));
    // Severity for the E2100 entries is ERROR.
    let has_error = last
        .diagnostics
        .iter()
        .any(|d| d.severity == Some(DiagnosticSeverity::ERROR));
    assert!(has_error, "expected at least one ERROR-severity diagnostic");
}


/// Helper: send a typed LSP request and decode the result into `T`.
async fn call_request<T: serde::de::DeserializeOwned>(
    service: &mut LspService<Backend>,
    method: &'static str,
    id: i64,
    params: impl serde::Serialize,
) -> T {
    let req = JsonRpcRequest::build(method)
        .id(id)
        .params(serde_json::to_value(params).unwrap())
        .finish();
    let resp = service
        .call(req)
        .await
        .unwrap()
        .unwrap_or_else(|| panic!("{method} returned no response"));
    let (_id, result) = resp.into_parts();
    let value = result.unwrap_or_else(|e| panic!("{method} response had error: {e:?}"));
    serde_json::from_value(value)
        .unwrap_or_else(|e| panic!("{method} result deserialise failed: {e}"))
}

/// Helper: poll `index.len()` until ≥ `threshold` or `timeout` elapses.
/// Returns the final len (so the caller can include it in failure msgs).
async fn wait_for_index(index: &Index, threshold: usize, timeout: Duration) -> usize {
    let start = std::time::Instant::now();
    while start.elapsed() < timeout {
        let n = index.len();
        if n >= threshold {
            return n;
        }
        tokio::time::sleep(Duration::from_millis(100)).await;
    }
    index.len()
}


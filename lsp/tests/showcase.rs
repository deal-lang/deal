#[tokio::test]
async fn definition_lookup_specializes() {
    let (mut service, _docs, index, _sink) = spawn_service_full().await;
    initialize_with_root(&mut service, Some(SHOWCASE_ROOT)).await;
    // Wait for eager_parse to populate the cross-file index.
    let n = wait_for_index(&index, 20, Duration::from_secs(5)).await;
    assert!(
        n >= 10,
        "eager_parse did not populate the index within 5s (got {n} symbols)"
    );

    // Open battery.deal — the file containing `<<specializes>> ThermallyManaged`.
    let src = std::fs::read_to_string(SHOWCASE_BATTERY).expect("battery.deal");
    let uri = Url::from_file_path(std::fs::canonicalize(SHOWCASE_BATTERY).unwrap())
        .unwrap();
    did_open(&mut service, &uri, &src, 1).await;
    tokio::time::sleep(Duration::from_millis(50)).await;

    // Locate the byte offset of "ThermallyManaged" in the
    // <<specializes>> clause. battery.deal mentions the type twice —
    // once in the `import` statement and once as the specializes
    // referent — we want the SECOND occurrence (the reference site).
    let needle = "ThermallyManaged";
    let first = src.find(needle).expect("ThermallyManaged in battery.deal");
    let byte = src[first + needle.len()..]
        .find(needle)
        .map(|n| first + needle.len() + n)
        .expect("second ThermallyManaged (the <<specializes>> referent)");
    let (line, char_) = byte_to_line_char(&src, byte + 4); // mid-identifier

    let params = GotoDefinitionParams {
        text_document_position_params: TextDocumentPositionParams {
            text_document: TextDocumentIdentifier { uri: uri.clone() },
            position: Position::new(line, char_),
        },
        work_done_progress_params: WorkDoneProgressParams::default(),
        partial_result_params: PartialResultParams::default(),
    };
    let resp: GotoDefinitionResponse = call_request_optional(
        &mut service,
        "textDocument/definition",
        11,
        params,
    )
    .await
    .expect(
        "textDocument/definition returned null — index lookup miss for \
         ThermallyManaged. Likely cause: eager_parse incomplete OR \
         AST candidate extraction returned None.",
    );
    let loc = match resp {
        GotoDefinitionResponse::Scalar(l) => l,
        GotoDefinitionResponse::Array(mut v) => v.pop().expect("non-empty array"),
        GotoDefinitionResponse::Link(_) => panic!("unexpected Link variant"),
    };
    // Cross-file go-to-definition: the target uri must NOT be battery.deal —
    // ThermallyManaged is defined in interfaces/thermal.deal.
    let target_path = loc.uri.to_file_path().expect("file:// URI");
    assert!(
        target_path.ends_with("interfaces/thermal.deal"),
        "expected cross-file target interfaces/thermal.deal, got {}",
        target_path.display()
    );
    // Range must be inside the target file (not empty / not bogus).
    assert!(
        loc.range.end.line >= loc.range.start.line,
        "target range is malformed: {:?}",
        loc.range
    );
}


/// Collected notifications (publishDiagnostics + other server-to-client
/// messages) from the LspService's ClientSocket.
type NotificationSink = Arc<Mutex<Vec<JsonRpcRequest>>>;

/// Spawn the LspService and drain ClientSocket on a background task.
/// Returns the service handle (for sending requests) and the shared
/// notification sink.
async fn spawn_service() -> (LspService<Backend>, Arc<Documents>, NotificationSink) {
    let (service, _docs, _idx, sink) = spawn_service_full().await;
    (service, _docs, sink)
}

/// Like `spawn_service` but also surfaces the Index so Plan 04 tests
/// can observe in-memory symbol-table state.
async fn spawn_service_full() -> (
    LspService<Backend>,
    Arc<Documents>,
    Arc<Index>,
    NotificationSink,
) {
    let (service, socket) = LspService::new(Backend::new);
    // Snapshot the Documents + Index Arcs so tests can introspect.
    let documents = service.inner().documents.clone();
    let index = service.inner().index.clone();

    let notifications: NotificationSink = Arc::new(Mutex::new(Vec::new()));
    {
        let sink = notifications.clone();
        tokio::spawn(async move {
            let mut socket = socket;
            while let Some(req) = socket.next().await {
                sink.lock().await.push(req);
            }
        });
    }

    (service, documents, index, notifications)
}

/// Send `initialize` so the server transitions to Initialized state
/// (otherwise tower-lsp rejects subsequent requests with "server not
/// initialized" responses).
async fn initialize(service: &mut LspService<Backend>) {
    initialize_with_root(service, None).await;
}

/// Like `initialize` but declares a workspace folder root URI so the
/// `initialized` handler kicks off the eager_parse background task
/// (Plan 03-04 Task 1).
async fn initialize_with_root(service: &mut LspService<Backend>, root: Option<&str>) {
    let workspace_folders = root.map(|p| {
        let abs = std::fs::canonicalize(p).expect("workspace root canonicalize");
        let uri = Url::from_file_path(&abs).expect("workspace folder URL");
        vec![WorkspaceFolder {
            uri,
            name: "showcase".to_string(),
        }]
    });
    let init_req = JsonRpcRequest::build("initialize")
        .id(1)
        .params(
            serde_json::to_value(InitializeParams {
                capabilities: ClientCapabilities::default(),
                workspace_folders,
                ..Default::default()
            })
            .unwrap(),
        )
        .finish();
    let _: Option<JsonRpcResponse> = service.call(init_req).await.unwrap();

    let initialized_req = JsonRpcRequest::build("initialized")
        .params(serde_json::to_value(InitializedParams {}).unwrap())
        .finish();
    let _: Option<JsonRpcResponse> = service.call(initialized_req).await.unwrap();
}

async fn did_open(service: &mut LspService<Backend>, uri: &Url, text: &str, version: i32) {
    let params = DidOpenTextDocumentParams {
        text_document: TextDocumentItem {
            uri: uri.clone(),
            language_id: "deal".to_string(),
            version,
            text: text.to_string(),
        },
    };
    let req = JsonRpcRequest::build("textDocument/didOpen")
        .params(serde_json::to_value(params).unwrap())
        .finish();
    let _: Option<JsonRpcResponse> = service.call(req).await.unwrap();
}

async fn did_change(service: &mut LspService<Backend>, uri: &Url, text: &str, version: i32) {
    let params = DidChangeTextDocumentParams {
        text_document: VersionedTextDocumentIdentifier {
            uri: uri.clone(),
            version,
        },
        content_changes: vec![TextDocumentContentChangeEvent {
            range: None,
            range_length: None,
            text: text.to_string(),
        }],
    };
    let req = JsonRpcRequest::build("textDocument/didChange")
        .params(serde_json::to_value(params).unwrap())
        .finish();
    let _: Option<JsonRpcResponse> = service.call(req).await.unwrap();
}

async fn formatting(service: &mut LspService<Backend>, uri: &Url) -> Option<Vec<TextEdit>> {
    let params = DocumentFormattingParams {
        text_document: TextDocumentIdentifier { uri: uri.clone() },
        options: FormattingOptions {
            tab_size: 4,
            insert_spaces: true,
            ..Default::default()
        },
        work_done_progress_params: Default::default(),
    };
    let req = JsonRpcRequest::build("textDocument/formatting")
        .id(99)
        .params(serde_json::to_value(params).unwrap())
        .finish();
    let resp = service
        .call(req)
        .await
        .unwrap()
        .expect("formatting returned None response");
    // resp.result() returns Option<&Value>; decode to Option<Vec<TextEdit>>.
    let (_id, result) = resp.into_parts();
    let value = result.expect("formatting response had no result");
    serde_json::from_value(value).expect("formatting result deserialise failed")
}

/// Helper: find all publishDiagnostics notifications in the sink.
async fn collected_diagnostics(sink: &NotificationSink) -> Vec<PublishDiagnosticsParams> {
    let mut out = Vec::new();
    for req in sink.lock().await.iter() {
        if req.method() == "textDocument/publishDiagnostics" {
            if let Some(params) = req.params() {
                if let Ok(p) = serde_json::from_value::<PublishDiagnosticsParams>(params.clone()) {
                    out.push(p);
                }
            }
        }
    }
    out
}


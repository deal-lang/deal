//! Plan 03-03 Task 3 integration tests for deal-lsp.
//!
//! Drives the `Backend` through tower-lsp's `LspService` so the full
//! request → server → client pipeline is exercised. The ClientSocket
//! receiver is drained on a background task into a shared notifications
//! Vec; tests assert against that collected stream after performing the
//! relevant LSP request.
//!
//! Three required tests (one per `<behavior>` bullet in 03-03-PLAN.md):
//!
//! 1. `diagnostics_match_phase_2_codes` — open a malformed showcase file;
//!    assert publishDiagnostics arrives carrying ≥1 E-code from the
//!    Phase 2 envelope (D-32 / D-33).
//!
//! 2. `format_round_trip` — open the canonical battery.deal showcase
//!    file; send textDocument/formatting; assert returned TextEdit.new_text
//!    equals the committed golden byte-for-byte (Issue 6 — golden was
//!    generated by `deal fmt --stdout` BEFORE this test runs; the test
//!    is a regression detector, not an in-test recomputation).
//!
//! 3. `debounce_collapses_rapid_changes` — open a file, fire 5 did_change
//!    events at sub-100ms intervals, then wait past the 300ms debounce
//!    window; assert exactly two parses ran (1 for open, 1 for the
//!    coalesced re-parse).
//!
//! NOTE on showcase path: Plan 03-03-PLAN.md references
//! `tests/showcase/sedan_project/vehicle/electrical/BatteryPack.deal`,
//! which does NOT exist in this repo (Rule 3 deviation). The closest
//! match — and the canonical showcase battery model — is
//! `spec/examples/showcase/packages/vehicle/battery.deal`. Documented
//! in the Plan SUMMARY as deviation; downstream Plan 03-04 / 03-07 work
//! references this file already.


// Test 3: debounce_collapses_rapid_changes
// ---------------------------------------------------------------------

#[tokio::test]
async fn debounce_collapses_rapid_changes() {
    let (mut service, documents, _sink) = spawn_service().await;
    initialize(&mut service).await;

    // Synthetic in-memory source (no fs dependency).
    let uri = Url::parse("file:///debounce.deal").unwrap();
    did_open(&mut service, &uri, "package debounce;", 1).await;
    let parses_after_open = documents.parse_count();
    assert_eq!(parses_after_open, 1, "did_open should trigger exactly one parse");

    // Fire 5 did_change events within the debounce window (300ms).
    for v in 2i32..=6 {
        did_change(
            &mut service,
            &uri,
            &format!("package debounce; // v{v}"),
            v,
        )
        .await;
        tokio::time::sleep(Duration::from_millis(30)).await;
    }
    // None of the 5 should have fired yet (only ~120ms elapsed).
    let parses_mid_burst = documents.parse_count();
    assert_eq!(
        parses_mid_burst, 1,
        "expected 0 debounced parses during burst, got {} extra",
        parses_mid_burst - 1
    );

    // Wait comfortably past the debounce window.
    tokio::time::sleep(Duration::from_millis(DEBOUNCE_MS + 100)).await;
    let parses_after_debounce = documents.parse_count();
    assert_eq!(
        parses_after_debounce, 2,
        "expected exactly one coalesced re-parse after debounce (open=1 + debounced=1), got {parses_after_debounce}"
    );
}

